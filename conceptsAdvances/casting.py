# castear a un entero int()
res = int('3') # -> 3
res2 = int(2.8) # -> 2
res3 = int(1) # -> 1

print(res, res2, res3)
# castear a un texto str()
res4 = str(1) # -> '1'
res5 = str(2.8) # -> '2.8'
d = str({"holi": 1, "hola": 2})

print(res4, res5, d)

# castear a un float()
res6 = float(1) # -> 1.0
print(res6)